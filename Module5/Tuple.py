# Tuple : Build in Data Type thats store multiple data type like list 
# Enclosed within Closed Brackets ()
# Immutable 

tup = (10,2,90,"Rahul" , 'q' , "Rahul")
print(tup)
print(tup[3])

# Empty Tupe
emptyTuple = ()
print(type(emptyTuple))

# Single Tuple
singleTuple = (1,)
print(type(singleTuple))

print(tup.index("Rahul"))
print(tup.count("Rahul"))

